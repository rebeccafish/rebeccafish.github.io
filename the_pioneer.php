	<h4> Design</h4>
	<p>I prepared mockups in Fireworks that addressed our objectives. The new layout was neat and colorful, with a grid structure reminiscent of the multi-column layouts of traditional newspapers. <span class="pub">The Pioneer</span>â€™s beautiful artwork was a focal point.</p>
	
