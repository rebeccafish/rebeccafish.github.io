	<h4> Development </h4>
	<p>I built the site using Wordpress as the CMS and a MySQL database as the content repository. Wordpress provided a search engine-friendly template structure with clean page titles and permalinks and took care of the worst of the database administration.  Another benefit was the built-in administration portal, which provided a browser-based workflow for <span class="pub">Pioneer</span> staff to write/edit articles and article metadata.</p>
	
	<p>I used the Wordpress codex and function library to build around twenty template pages, including individualized section pages (News, Sports, A&amp;E, etc.). The stylesheets were written in <a href="http://lesscss.org/" target="_blank">LESS</a>, a dynamic stylesheet language, which kept the styles concise. It also made them versatile if the site was overhauled by new editors down the line.</p>
</div></section>
