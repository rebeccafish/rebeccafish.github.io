	<h2>Financial company iPad app</h2>
	<p>A major financial services company in NYC that specializes in online stock brokerage wanted a complete overhaul of its iPad app.  The new app would display the user's accounts and portfolio. It also would provide a number of research tools and allow users to buy and sell stock.</p>
	<p>I worked with the Senior UI Designer to develop wireframes for the app.  Generally speaking, he did the information architecture and high-level structure wireframes, and I worked on detail screens and low-level complexities.</p>
	
	<p>A comprehensive requirements doc was drawn up with the client before the initial wireframe stage.  Early in the process, I listened to a number of customer interviews. I also did competitive research and an audit of both the previous app and an earlier in-house proposal (done before the client decided to reach out to an agency).  </p>
	<p>The final version of the product has recently been released into the iTunes App Store.</p>
